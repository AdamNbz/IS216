    private void btn_TimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_TimActionPerformed
        String ma = tf_maSach.getText().trim();
        if (ma.isEmpty()) 
        {
            JOptionPane.showMessageDialog(this, "Nhập mã sách cần tìm.");
            return;
        }
        Sach s = ds.timSach(ma);
        if (s == null) 
        {
            JOptionPane.showMessageDialog(this, "Không tìm thấy sách mã " + ma);
            return;
        }
        
        tf_tenSach.setText(s.getTenSach());
        tf_tacGia.setText(s.getTacGia());
        tf_NXB.setText(s.getNhaXuatBan());
        tf_price.setText(String.valueOf(s.getGia()));
        
        DefaultTableModel model = (DefaultTableModel) tbl_Sach.getModel();
        for (int i = 0; i < model.getRowCount(); i++) 
        {
            if (model.getValueAt(i,0).equals(ma)) 
            {
                tbl_Sach.setRowSelectionInterval(i, i);
                break;
            }
        }
    }//GEN-LAST:event_btn_TimActionPerformed

