    private DSSach ds = new DSSach();
    
    private void refreshTable() 
    {
        DefaultTableModel model = (DefaultTableModel) tbl_Sach.getModel();
        model.setRowCount(0);
        for (int i = 0; i < ds.getSLSach(); i++) 
        {
            Sach s = ds.laySach(i);
            model.addRow(new Object[]{s.getMaSach(), s.getTenSach(), s.getTacGia(), s.getNhaXuatBan(), s.getGia()});
        }
    }

    private void clearInputs() 
    {
        tf_maSach.setText("");
        tf_tenSach.setText("");
        tf_tacGia.setText("");
        tf_NXB.setText("");
        tf_price.setText("");
        tbl_Sach.clearSelection();
    }
    
    private boolean validateInputs() 
    {
        if (tf_maSach.getText().trim().isEmpty()
         || tf_tenSach.getText().trim().isEmpty()
         || tf_tacGia.getText().trim().isEmpty()
         || tf_NXB.getText().trim().isEmpty()
         || tf_price.getText().trim().isEmpty()) 
        {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin.");
            return false;
        }
        try 
        {
            Double.parseDouble(tf_price.getText().trim());
        } 
        catch (NumberFormatException nfe) 
        {
            JOptionPane.showMessageDialog(this, "Giá phải là số hợp lệ.");
            return false;
        }
        return true;
    }
    
    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        if (!validateInputs()) return;
        Sach s = new Sach(
            tf_maSach.getText().trim(),
            tf_tenSach.getText().trim(),
            tf_tacGia.getText().trim(),
            tf_NXB.getText().trim(),
            Double.parseDouble(tf_price.getText().trim())
        );
        if (!ds.themSach(s)) 
        {
            JOptionPane.showMessageDialog(this, "Mã sách đã tồn tại.");
            return;
        }
        refreshTable();
        clearInputs();
    }//GEN-LAST:event_btn_ThemActionPerformed

    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed
        int row = tbl_Sach.getSelectedRow();
        if (row < 0) 
        {
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 dòng để xóa.");
            return;
        }
        String ma = tbl_Sach.getValueAt(row, 0).toString();
        int ok = JOptionPane.showConfirmDialog(this, "Chắc chắn xóa sách mã "+ma+"?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (ok != JOptionPane.YES_OPTION) return;
        if (!ds.xoaSach(ma)) 
        {
            JOptionPane.showMessageDialog(this, "Không tìm thấy mã để xóa.");
            return;
        }
        refreshTable();
        clearInputs();
    }//GEN-LAST:event_btn_XoaActionPerformed

    private void btn_SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuaActionPerformed
        int row = tbl_Sach.getSelectedRow();
        if (row < 0) 
        {
            JOptionPane.showMessageDialog(this, "Hãy chọn 1 dòng để sửa.");
            return;
        }
        if (!validateInputs()) return;
        String ma = tf_maSach.getText().trim();
        Sach s = new Sach(
            ma,
            tf_tenSach.getText().trim(),
            tf_tacGia.getText().trim(),
            tf_NXB.getText().trim(),
            Double.parseDouble(tf_price.getText().trim())
        );
        int ok = JOptionPane.showConfirmDialog(this, "Lưu sửa cho sách mã "+ma+"?", 
                                               "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (ok != JOptionPane.YES_OPTION) return;
        if (!ds.capNhatDSSach(ma, s)) 
        {
            JOptionPane.showMessageDialog(this, "Không tìm thấy mã để sửa.");
            return;
        }
        refreshTable();
        clearInputs();
    }//GEN-LAST:event_btn_SuaActionPerformed

